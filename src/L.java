private boolean validateLogin(String id, String password) {
    String query = "SELECT * FROM users WHERE id = ? AND password = ?";
    try (Connection connection = DriverManager.getConnection("jdbc:mysql://localhost:3306/diu_bloodbank", "root", "");
         PreparedStatement stmt = connection.prepareStatement(query)) {

        // Set the parameters for the query
        stmt.setString(1, id);
        stmt.setString(2, password);

        // Execute the query
        ResultSet rs = (ResultSet) stmt.executeQuery();

        // If a record is found, the login is valid
        return rs.next();
    } catch (SQLException ex) {
        ex.printStackTrace();
        return false; // Returns false if there's a database error
    }
}


