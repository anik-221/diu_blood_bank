    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        
    String id = jTextField1.getText(); // Assume jTextField1 is for ID
    String password = new String(jPasswordField1.getPassword()); // Assume jPasswordField1 is for password

    if (id.isEmpty() || password.isEmpty()) {
        javax.swing.JOptionPane.showMessageDialog(this, "Please fill in all fields.", "Error", javax.swing.JOptionPane.ERROR_MESSAGE);
        return;
    }

    // Validate credentials
    if (validateLogin(id, password)) {
        javax.swing.JOptionPane.showMessageDialog(this, "Login successful!", "Success", javax.swing.JOptionPane.INFORMATION_MESSAGE);
        this.dispose(); // Close the login page
        new Home().setVisible(true); // Open the Home page
    } else {
        javax.swing.JOptionPane.showMessageDialog(this, "Invalid ID or password.", "Login Failed", javax.swing.JOptionPane.ERROR_MESSAGE);
    }
        
        
    }//GEN-LAST:event_jButton1ActionPerformed

