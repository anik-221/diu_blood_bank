    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        
        String id = jTextField1.getText(); // Get ID from text field
    if (id.isEmpty()) {
        javax.swing.JOptionPane.showMessageDialog(null, "Please enter a valid ID.");
        return;
    }

    int confirm = javax.swing.JOptionPane.showConfirmDialog(null, "Are you sure you want to delete this donor?", "Confirm Delete", javax.swing.JOptionPane.YES_NO_OPTION);
    if (confirm == javax.swing.JOptionPane.YES_OPTION) {
        String query = "DELETE FROM Donors WHERE id = ?";
        try (Connection connection = DriverManager.getConnection("jdbc:mysql://localhost:3306/diu_bloodbank", "root", "");
             PreparedStatement stmt = connection.prepareStatement(query)) {

            stmt.setString(1, id); // Set the ID in the query
            int rowsAffected = stmt.executeUpdate();

            if (rowsAffected > 0) {
                javax.swing.JOptionPane.showMessageDialog(null, "Donor deleted successfully!");
                clearFields(); // Clear all fields
            } else {
                javax.swing.JOptionPane.showMessageDialog(null, "Donor not found!");
            }
        } catch (SQLException ex) {
            ex.printStackTrace();
            javax.swing.JOptionPane.showMessageDialog(null, "Database error: " + ex.getMessage());
        }
    }
        
    }//GEN-LAST:event_jButton2ActionPerformed

