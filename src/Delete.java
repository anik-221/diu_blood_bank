    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        
        String id = jTextField1.getText(); // Get ID from text field
    if (id.isEmpty()) {
        javax.swing.JOptionPane.showMessageDialog(null, "Please enter a valid ID.");
        return;
    }

    String query = "SELECT * FROM Donors WHERE id = ?";
    try (Connection connection = DriverManager.getConnection("jdbc:mysql://localhost:3306/diu_bloodbank", "root", "");
         PreparedStatement stmt = connection.prepareStatement(query)) {

        stmt.setString(1, id); // Set the ID in the query
        ResultSet rs = stmt.executeQuery();

        if (rs.next()) {
            // Populate fields with data from the database
            jTextField2.setText(rs.getString("name"));
            jTextField6.setText(rs.getString("id"));
            jTextField3.setText(rs.getString("phone"));
            jComboBox2.setSelectedItem(rs.getString("member_type"));
            jComboBox3.setSelectedItem(rs.getString("gender"));
            jComboBox1.setSelectedItem(rs.getString("blood_group"));
            jTextField4.setText(rs.getString("last_donation_date"));
            jTextField5.setText(rs.getString("location"));
        } else {
            javax.swing.JOptionPane.showMessageDialog(null, "Donor not found!");
        }
    } catch (SQLException ex) {
        ex.printStackTrace();
        javax.swing.JOptionPane.showMessageDialog(null, "Database error: " + ex.getMessage());
    }
        
    }//GEN-LAST:event_jButton1ActionPerformed

